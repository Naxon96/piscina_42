#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	h;

	i = 0;
	h = argc;
	while (argv[0][i] != '\0')
	{
		write(1, &argv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
